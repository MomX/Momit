#' Example datasets and helpers
#'
#' [Momit_data] is wraps [system.file] to ease Momit examples. Below is a list of datasets
#' provided with the package and the list of the generous providers.
#'
#' @details
#' You do not need [Momit_data] for _your_ data.
#' Most of Momit examples use these functions in examples and vignettes.
#' For package testing, R CMD CHECK etc., it is required to find correct locations of these files.
#'
#' @param x `character` file(s) names from the example folder, only for `example_data`
#' @return `character` the full path pointing to `Momit/extdata/`
#'
#' @references
#' `Momit_data` is adapted from [R-pkg](http://r-pkgs.had.co.nz/data.html#data-extdata) by Hadley Wickham.
#'
#' Example files are organized in subfolders and data providers are listed below. Thanks a lot to them!
#'
#' * `.chc` file in `chc/` was generated by SHAPE by Hisato Iwata and colleagues and generously shared by Callie Crawford
#' * `.tps` files in `tps/` were generated using tpsDig by James Rohlf and generously provided by Allowen Evin (`AE/`), Julien Claude and Coline Verot (`JC/`)
#' * `.txt` files were generated using ImageJ by myself.
#'
#' The `vitis/` folder is intended as a minimal case of a real world database.
#' It is at heart of the "Survival guide for handling a database" vignette (todo).
#' `jpg` files in `raw/` come from a huge collection of modern grapevine pips and
#' were generously provided by Laurent Bouby, Sarah Ivorra,
#' Vincent Bonhomme, Clémence Pagnoux, Thierry Pastor and Jean-Frédéric Terral. Three folders of this database
#' were extracted with only 3 individuals. Image size was largely decreased to cope with CRAN standards.
#' Structure of filenames was generously messed up. `csv` in `meta/` is a toy example of a covariate spreadsheet.
#'
#' @examples
#'  # List all example data
#'  Momit_data() %>% sniff()
#'
#' @aliases datasets
#' @name example_data
#' @export
Momit_data <- function(x=""){
  system.file("extdata", x, package = "Momit")
}



